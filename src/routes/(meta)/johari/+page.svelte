<h2>The Johari Window</h2>

<p>The <b>Johari window</b> is a heuristic exercise created in 1955, in which you and your peers explore how you each perceive yourself and one another. It was built as a tool for self-awareness, and today is used <a href="/sources#mind-tools">as a team-building exercise</a>.</p>

<div class="highlight thin">
    <p>The Johari window was created by <b>Joseph Luft</b> and <b>Harry Ingham</b>. "Johari" is a portmanteau of their first names.</p>
</div>

<p>One challenge of the Johari window is to learn about and accept <em>both</em> your strengths and weaknesses. It shares that with <a href="/lesson/4">acceptance and commitment therapy</a>, a form of therapy that teaches the acceptance of our internal experiences, without combatting negative sensations.</p>

