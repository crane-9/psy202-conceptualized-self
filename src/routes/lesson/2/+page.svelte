<section class="padded align-center thin">
    <h2>The Weaknesses of the Conceptualized Self</h2>

    <p>Similar to labels, a strong conceptualized self can be limiting. We can become <b>psychologically inflexible</b>, holding ourselves to how we have defined ourselves. This can be a source of excuses for ourselves and for others.</p>
    
    <FloatingExamples>
        <span>You know how <strong>forgetful</strong> I am.</span>
        <span>But I'm not <strong>creative</strong> like you!</span>
        <span>Don't be offended, he's just <strong>edgy</strong> like that.</span>
    </FloatingExamples>
    
    <p>On the other hand, we are sometimes <strong>overconfident</strong> in our conceptualized self. Someone who believes that they are patient and thoughtful might not be patient and thoughtful in a high-stress situation when it is most needed. Or, we may pride ourselves on the idea of who we are without following through on those positive qualities.</p>
    
    <p>Inversely, we sometimes build a strong <strong><em>negative</em> self-image</strong> through the conceptualized self. We might define ourselves by past mistakes, shame, or trauma. The excessive assessment of the self has <a href="/sources#noureen">recently been found to correlate with depressive symptoms</a>.</p>
</section>

<section class="highlight thin">
    <h2>What are the weaknesses of the present-moment self and observing self?</h2>

    <p>The present-moment self may lack foresight, or critical planning skills. Only living in the present, in today's world, is incompatible with adult responsibilities. Some strengths of living in the present are enjoyment and stress-relief.</p>

    <p>The observing self does not hold judgment. Acting solely with the observing self, we may not know how to respond in social situations, or how to pursue a life goal.</p>
</section>

