<section id="one" class="center-flex align-center thin padded">
    <h2>What is the conceptualized self?</h2>
    
    
    <p>Put simply, the conceptualized self is the version of yourself that embodies every idea you have about yourself and your past. It is the version of yourself you describe when someone asks you to tell you about yourself, or the version of yourself you assess when taking a personality quiz.</p>
    
    <p>A simple example is when we say something along the lines of: "I am easily distracted".</p>

    <p>It is also called the "autobiographical self" or "narrative self"</p>
</section>

<section class="highlight thin">
    <h2>Why is it the "conceptualized self" and not "self-concept"?</h2>

    <p>This is because the "conceptualized self" is only one version of yourself. According to Antonio Damasio's model, you also have a <VocabWord message="The 'present-moment self' is the mode of self that lives in the moment, and is connected to present stimulus in the environment.">present-moment self</VocabWord> and an <VocabWord message="The 'observing self' is the mode of self that quietly observes one's own thoughts and feelings.">observing self</VocabWord>. These two modes of self, as opposed to the conceptualized self, exist without judgment, and in the moment. The present-moment self cares more about what you are experiencing, and not discerning or judging what it means for your story as a person.</p>

    <p>Read more: <a href="https://psycnet.apa.org/record/2010-24474-000">Antonio Damasio's <cite>Self Comes to Mind</cite></a>.</p>
</section>

<section class="padded semi-thin">
    <h2>Strengths of the conceptualized self:</h2>
    
    <ol>
        <li><b>The conceptualized self helps you to understand your wants.</b> A conceptualized self helps you to understand what makes you happy, and to predict what would (or wouldn't be) fulfilling for you in life. It may also make it easier to connect to and relate with others — for instance, joining a club based on a common interest: how a hiker may relate to other hikers.</li>
        
        <li><b>Fosters insight into others.</b> With insight into the self, you may have deeper insight into others. A connection with your conceptualized self may lead to deeper and longer connections with others.</li>
        
        <li><b>Gives you a reference to the past and future.</b> The conceptualized self is built from personal life events and experiences, grounding you in time. The conceptualized self may be what you consult when trying to envision your future: your desires, your flaws, your strengths, and your past.</li>
        
        <li><b>Helps with planning.</b> Similar to giving you an anchor in time, thinking with the conceptualized self helps you to plan ahead, and motivates you to select delayed gratification over immediate reward.</li>
        
        <li><b>Simplification.</b> Sometimes, it is easier to understand yourself as a collection of traits. It works well for resumés.</li>
    </ol>
</section>

