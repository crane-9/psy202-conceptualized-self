<section class="padded center-flex align-center thin">
    <h2>Treating cognitive fusion and a negative conceptualized self</h2>

    <p>While the conceptualized self is not the enemy, it has been shown that a <b>flexible</b> conceptualized self is vital to mental health. It is healthy to keep an open mind about our own thoughts, feelings, judgments, and allow ourselves to feel what we feel, rather than evaluating an unpleasant internal experience as something that must define us. </p>
</section>

<section class="highlight thin">
    <h2>Acceptance and commitment therapy</h2>

    <p><VocabWord message="A form of therapy targeted toward treating psychological inflexibility. It is a form of positive or humanistic psychology, focused toward growth rather than supressing negative emotions.">Acceptance and commitment therapy</VocabWord> (ACT) is an emotion-focused form of therapy that emphasizes <VocabWord message="Psychological flexibility is our ability to adapt our thoughts to external changes. For example, changing your mind when presented with contrary evidence. Psychological flexibility has been shown to help decrease levels of depression in some.">psychological flexibility</VocabWord>. One goal of ACT is to become less reactive, and to act in ways inline with the values we choose.</p>

    <p>Skills taught by ACT:</p>

    <ol>
        <li><b>Cognitive defusion.</b> The reframing of cognitive experiences as experiences, and not the truth. For example reframing "I am anxious", as "I am experiencing anxiety". The effect of human language on our cognition is explored by <VocabWord message="Relational Frame Theory (RTF) is a theory of the relationship between human language, behavior, and cognition. It poses that the relationship between language, behavior, and cognition, is the foundation of meaning for those very things.">Relational Frame Theory</VocabWord>.</li>
        <li><b>Acceptance.</b> Acceptance is a simple concept, but key to ACT. Rather than fighting with unpleasant emotions, ACT encourages the acceptance of these experiences. Acceptance in itself is not a solution or "cure" to negative thought patterns, but a path to more thoughtful actions.</li>
        <li><b>Being present.</b> Disconnecting from the conceptualized self, and coming closer to the present-moment self, helps us to get out of our heads.</li>
    </ol>

    <InfoBox>
        <p>Like any form of therapy, this is not a one-size-fits-all. It is important to note that not all problems can be treated with mindfulness.</p>
    </InfoBox>
</section>

<section class="padded center-flex align-center semi-thin">
    <h2>Reminder: The observing self</h2>

    <p>The observing self is the part of us that hears our thoughts without evaluating or judging. Where the conceptualized self may compare a thought  with our perceived life narrative or who we expect ourselves to be, the observing self acknowledges and accepts the experience of thought or feeling.</p>

    <div class="align-left highlight thin">
        <p>For example:</p>

        <p><b>The thought:</b> "I'm bored and I want to go home."</p>
        <p><b>Who we expect to be:</b> "This is my sister's party, I love my sister and I should be having fun."</p>
        <p><b>The acknowledgment:</b> "I'm feeling bored and wanting to go home, but I also feel that I should stay for my sister's party."</p>
    </div>
</section>

<section class="padded thin align-center">
        <p>You might see that accepting a feeling <b>does not resolve the situation</b>: <a href="/sources#hayes">that is not the goal of ACT strategies</a>. Instead, accepting emotions as they come helps the brain to become less reactive over time, thanks to <VocabWord message="Neuroplasticity is the brain's ability to build or strengthen neural pathways over time with practice. It's like exercising a muscle, or building a highway.">neuroplasticity</VocabWord>.</p>
    
        <p>As we become more psychologically flexible, we show less cognitive fusion, which gives our sense of conceptualized self less power over us. <b>In this way, ACT can help treat the effects of a negative conceptualized self, without needing to tackle how we see ourselves.</b></p>
</section>
