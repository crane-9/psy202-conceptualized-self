<section class="padded center-flex thin">
    <h2>The conceptualized self and depressive symptoms</h2>

    <p>In <a href="/sources#noureen">a 2019 study</a>, 200 university students (ages 19 - 32) from the University of Sargodha, in Pakistan, were surveyed regarding their experiences with the conceptualized self, <VocabWord message="The inability to separate subjective thoughts from one's perception of reality. When a thought becomes difficult to separate oneself from.">cognitive fusion</VocabWord>, and depressive symptoms.</p>

    <p>The study broke the conceptualized self down into three forces:</p>
    
    <ul>
        <li><b>Reassured-self</b>. A positive sense of self that assures you in stressful times.</li>
        <li><b>Inadequate-self</b>. A negative sense of self that claims you are not good enough, sub-par. Self-doubt.</li>
        <li><b>Hated-self</b>. A deeply negative sense of self that claims you are undesirable, unlovable, and may wish some form of harm towards yourself.</li>
    </ul>
    
    <div class="flex-row">
        <i class="fa-solid fa-heart"></i>
        <i class="fa-solid fa-face-frown"></i>
        <i class="fa-solid fa-person-harassing"></i>
    </div>
    
    <p>This study revealed that students with "critical selves" (inadequate-self and/or hated-self) were more likely to be experiencing depressive symptoms, while students with a stronger sense of reassured-self were less likely.</p>
</section>

<section class="highlight thin">
    <p><b>Cognitive fusion</b> is when we get lost in our own subjective reality, to the point where we see our own thoughts, judgements, perspectives, and beliefs as the truth. Reality becomes clouded over.</p>

    <FloatingExamples>
        <span>"I can't do that, I'm ____."</span>
        <span>"He must be upset with me. That's the only explanation."</span>
    </FloatingExamples>

    <p>Cognitive fusion is a form of <VocabWord message="Psychological inflexibility is an umbrella of mental processes in which an individual struggles to act independent of the influence of thoughts and feelings.">psychological inflexibility</VocabWord>. Behavior becomes guided more by internal experiences than evaluation of the external world, long-term goals, or even personal values.</p>
</section>

<section class="highlight semi-thin">
    <h2>So What was the role of cognitive fusion?</h2>

    <p>Cognitive fusion was found to serve as a mediator between the conceptualized self and depressive symptoms. </p>

    <p>Though the conceptualized self is only one perspective we may have of ourselves, it is one tied deeply to our internal experience (thoughts, feelings, perceptions, beliefs, and self-talk). When we have a negative sense of conceptualized self, we likely experience a lot of negative self-talk, which cognitive fusion frames as the objective truth. Perceptions and feelings become perceived-reality, which in turn build into or exacerbate depressive symptoms.</p>

    <FloatingExamples>
        <span>A stubborn sense of self leads to more cognitive fusion,</span>
        <span>Which may build into depressive symptoms.</span>
    </FloatingExamples>

    <p>However, this study found that cognitive fusion seemed to suppress the reassured-self, rather than reinforce. <b>Even for students with a positive view of themselves, the inflexibility of cognitive fusion contributed to depressive symptoms.</b></p>
</section>

